#ifndef EASYFIND_HPP
# define EASYFIND_HPP

# include <iostream>
# include <algorithm>
# include <vector>

template<typename T>
int		&easyfind(T &array, int tofind)
{
	typename T::iterator	it;

	it = std::find(array.begin(), array.end(), tofind);
	if (it != array.end())
		return (*it);
	throw std::exception();
}

#endif
